    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        StudentGrade sg=new StudentGrade();
        sg.setCourseID (Integer.parseInt(jTextField1.getText()));
        
        sg.setStudentID(Integer.parseInt(jTextField2.getText()));
        sg.setGrade(Float.parseFloat(jTextField3.getText()));
        int a=Integer.parseInt(jTextField2.getText());
        try {
            System.out.print(a);
            
            if(!sgBLL.isStudentIDExist(a)){
                if(sgBLL.addStudentGrade(sg)>0){
                 JOptionPane.showMessageDialog(this, "Complete add student ", "Message", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                sgForm.loadStudentGrade(null);
                }
                else {
                    JOptionPane.showMessageDialog(this, "Error add student", "Message", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Student ID đã tồn tại.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        catch(SQLException ex){
            Logger.getLogger(StudentGradeAddForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addbtnActionPerformed
