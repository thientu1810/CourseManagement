    private void jTextField1InputMethodTextChanged(java.awt.event.InputMethodEvent evt) {//GEN-FIRST:event_jTextField1InputMethodTextChanged
         try {
        int studentID = Integer.parseInt(jTextField1.getText());
        String studentName = showName(studentID);
        jTextField4.setText(studentName);
    } catch (NumberFormatException ex) {
        jTextField4.setText("Invalid input");
    } catch (SQLException ex) {
        jTextField4.setText("Error fetching data");
    }

    }//GEN-LAST:event_jTextField1InputMethodTextChanged
    private String showName(int studentID) throws SQLException {
        return sgBLL.showName(studentID);
    }
