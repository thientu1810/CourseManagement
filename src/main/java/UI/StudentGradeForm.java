    protected void loadStudentGrade(ArrayList<StudentGrade> l) throws SQLException {
        ArrayList<StudentGrade> studentGradeList = new ArrayList<StudentGrade>();
        if (l == null) {
            studentGradeList = (ArrayList<StudentGrade>) sgBLL.LoadStudentGrade();
        } else {
            studentGradeList = l;
        }
        String[] colNames = {"EnrollmentID","Course Name","FirstName","LastName", "Grade"};
        Object[][] rows = new Object[studentGradeList.size()][colNames.length];
        for (int i = 0; i < studentGradeList.size(); i++) {
            rows[i][0] = studentGradeList.get(i).getEnrollmentID();
            rows[i][1] = studentGradeList.get(i).getTitle();
            rows[i][2]=studentGradeList.get(i).getFirstName();
            rows[i][3]=studentGradeList.get(i).getLastName();
            Float grade = studentGradeList.get(i).getGrade();
            rows[i][4] = (grade != null) ? grade : "NULL";
        }
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable1.setModel(model);
    }
