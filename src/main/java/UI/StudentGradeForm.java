    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        if (row != -1) {
            int studentID = Integer.parseInt(model.getValueAt(row, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa dòng này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                try {
                    if (sgBLL.deleteStudentGrade(studentID) > 0) {
                        JOptionPane.showMessageDialog(this, "Complete delete student", "Message", JOptionPane.INFORMATION_MESSAGE);
                        loadStudentGrade(null);
                    }
                }catch (SQLException ex) {
                    Logger.getLogger(StudentGradeForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }}else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để Xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_deletebtnActionPerformed

