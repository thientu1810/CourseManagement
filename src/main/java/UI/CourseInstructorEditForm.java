    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Object a = jComboBox1.getSelectedItem();
        String[] parts = a.toString().split("_");
        String result1 = parts[0];
        String tID = result1;
        int teacherID = Integer.parseInt(tID);
        
        Object b = jComboBox2.getSelectedItem();
        String[] parts2 = b.toString().split("_");
        String result2 = parts2[0];
        String cID = result2;
        int courseID = Integer.parseInt(cID);
        
        Entity.CourseInstructor ci = new Entity.CourseInstructor();
        ci.setCourseID(courseID);
        ci.setTeacherID(teacherID);
        
        if(Validate()) {
            bll.updateCourseInstructor(ci, old);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

