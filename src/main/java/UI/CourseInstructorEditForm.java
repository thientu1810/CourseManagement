    public CourseInstructorEditForm(int cid, int tid) throws SQLException {
        initComponents();
        
        old.setCourseID(cid);
        old.setTeacherID(tid);
        
        for(Entity.Teacher tc : tbll.LoadTeacher()) {
            jComboBox2.addItem(tc.getPersonId() + "_" + tc.getFirstName() + " " + tc.getLastName());
        }
        
        for(Entity.Course c : onl.LoadOnlineCourse()) {
            jComboBox1.addItem(c.getCourseID() + "_" + c.getTitle());
        }
        
        for(Entity.Course c : ons.LoadOnsiteCourse()) {
            jComboBox1.addItem(c.getCourseID() + "_" + c.getTitle());
        }
        
        try {
            Entity.Teacher tc = tbll.GetTeacher(tid);
            jComboBox2.setSelectedItem(tid + "_" + tc.getFirstName() + " " + tc.getLastName());
            
            jComboBox1.setSelectedItem(cid + "_" + bll.getTitleCourse(cid));
            
        } catch (SQLException ex) {
            Logger.getLogger(TeacherEditForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

