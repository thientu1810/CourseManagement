    protected void loadOnlineCourse(ArrayList<OnlineCourse> l) throws SQLException {
    ArrayList<OnlineCourse> onlineCourseList = new ArrayList<OnlineCourse>();
    if(l == null) {
       onlineCourseList = (ArrayList<OnlineCourse>) onlBLL.LoadOnlineCourse();
    } else {
        onlineCourseList = l;
    }
    for(int i=0; i< onlineCourseList.size(); i++){
        System.out.print(onlineCourseList.get(i).toString());
    }
    // jtable columns
    String[] colNames = {"COURSE ID","TITLE", "CREDITS", "URL", "DEPARTMENT ID"};
    
    // jtable rows
    Object[][] rows = new Object[onlineCourseList.size()][colNames.length];
    
    for(int i = 0; i < onlineCourseList.size(); i++) {
        rows[i][0] = onlineCourseList.get(i).getCourseID();
        rows[i][1] = onlineCourseList.get(i).getTitle();
        rows[i][2] = onlineCourseList.get(i).getCredits(); 
        rows[i][3] = onlineCourseList.get(i).getUrl();
        rows[i][4] = onlineCourseList.get(i).getDepartmentID();
    }
    
    DefaultTableModel model = new DefaultTableModel(rows, colNames);
    jTable1.setModel(model);
}

