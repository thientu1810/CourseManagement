    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        int row = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();

        int onlineCourseID = Integer.parseInt(model.getValueAt(row, 0).toString());
        System.out.print(onlineCourseID);
        try {
            if (onlBLL.deleteOnlineCourse(onlineCourseID) > 0) {
                JOptionPane.showMessageDialog(this, "Complete delete OnlineCourse", "Message", JOptionPane.INFORMATION_MESSAGE);
                loadOnlineCourse(null);
            } else {
                JOptionPane.showMessageDialog(this, "Error delete OnlineCourse", "Message", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(OnlineCourseForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

