    private void loadCI(List<Entity.CourseInstructor> cis) {
        jTable1.setModel(new DefaultTableModel());
        ArrayList<Entity.CourseInstructor> list = new ArrayList<Entity.CourseInstructor>();
        if(cis == null) {
           list = (ArrayList<Entity.CourseInstructor>) bll.LoadCourseInstructor();
        } else {
            list = (ArrayList<Entity.CourseInstructor>) cis;
        }
        
        // jtable columns
        String[] colNames = {"TEACHER-ID", "TEACHER", "COURSE-ID", "TITLE COURSE"};
        
        // jtable rows
        Object[][] rows = new Object[list.size()][colNames.length];
        
        for(int i = 0; i < list.size(); i++) {
            rows[i][0] = list.get(i).getTeacherID();
            rows[i][1] = list.get(i).getFirstName() + " " + list.get(i).getLastName();
            rows[i][2] = list.get(i).getCourseID();
            rows[i][3] = list.get(i).getTitleCourse();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable1.setModel(model);
    }
