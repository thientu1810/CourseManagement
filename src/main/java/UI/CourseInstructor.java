    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            int row = jTable1.getSelectedRow();
            TableModel model = jTable1.getModel();
            
            int tID = Integer.parseInt(model.getValueAt(row, 0).toString());
            
            int cID = Integer.parseInt(model.getValueAt(row, 2).toString());
            
            String s = "";
            
            BLL.OnlineCourseBLL onlbll = new OnlineCourseBLL();
            BLL.TeacherBLL tcbll = new TeacherBLL();
            BLL.OnsiteCourseBLL onsbll = new OnsiteCourseBLL();
            
            s += tcbll.GetTeacher(tID).toString() + "\n";
            
            if(onsbll.getOnsiteCourse(cID).getCourseID() > 0) s += onsbll.getOnsiteCourse(cID).toString() + "\n";
            
            if(onlbll.getOnlineCourse(cID).getCourseID() > 0) s += onlbll.getOnlineCourse(cID).toString();
            
            JOptionPane.showMessageDialog(null, s);
        } catch (SQLException ex) {
            Logger.getLogger(CourseInstructor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


